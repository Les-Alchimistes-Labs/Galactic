    public (Case.EnumType, EnumsItem)[,] matrixCase = new Case().ConvertMatrix(matrix);

    public void GenerateMap((Case.EnumType, EnumsItem)[,] matrix)
    {
        for (int indiceX = 0; indiceX < matrix.GetLength(0); indiceX++)
        {
            for (int indiceY = 0; indiceY < matrix.GetLength(1); indiceY++)
            {
                switch (matrix[indiceX, indiceY])
                {
                    case (Case.EnumType.Ground, EnumsItem.Empty):
                        GameObject ground = Instantiate(Biome1Prefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        ground.transform.SetParent(transform);
                        if (Random.Range(0,200) == 1 && !biomeUse[0] && indiceY>10 )
                        {
                            biomeUse[0] = true;
                            EnemyGenerator.EnemyGeneratore(EnumMonster.BossIntermediate,IntermediateMonster,null,level,indiceX,indiceY);
                        }
                        break;
                    case (Case.EnumType.Tree, EnumsItem.Empty):
                        GameObject tree = Instantiate(treePrefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        tree.transform.SetParent(transform);
                        break;
                    case (Case.EnumType.Wall, EnumsItem.Empty):
                        GameObject wall = Instantiate(wallPrefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        wall.transform.SetParent(transform);
                        break;
                    case (Case.EnumType.River, EnumsItem.Empty):
                        GameObject river = Instantiate(riverPrefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        river.transform.SetParent(transform);
                        break;
                    case (Case.EnumType.Item, EnumsItem.Empty):
                        GameObject item = Instantiate(itemPrefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        item.transform.SetParent(transform);
                        break;
                    case (Case.EnumType.Rock, EnumsItem.Empty):
                        GameObject rock = Instantiate(rockPrefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        rock.transform.SetParent(transform);
                        break;
                    default:
                        ground = Instantiate(Biome1Prefab, new Vector3(indiceX, 0, indiceY), Quaternion.identity);
                        ground.transform.SetParent(transform);
                        break;
                }
            }
        }
    }
    



